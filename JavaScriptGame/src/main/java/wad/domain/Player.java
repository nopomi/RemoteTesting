
package wad.domain;


public class Player {

    private int score;
    private String name;

    public String getName() {
        return this.name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Integer getScore() {
        return this.score;
    }

    public void setScore(int score) {
        this.score = score;
    }

}
